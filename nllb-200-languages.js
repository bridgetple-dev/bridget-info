// Helper functions
function getLanguageByCode(code) {
  return NLLB_200_LANGUAGES.find(lang => lang.code === code);
}

function getAvailableLanguages() {
  return NLLB_200_LANGUAGES.filter(lang => lang.hasTranslation);
}

function getMissingLanguages() {
  return NLLB_200_LANGUAGES.filter(lang => !lang.hasTranslation);
}

function getLanguagesByTier(tier) {
  return NLLB_200_LANGUAGES.filter(lang => lang.tier === tier);
}

